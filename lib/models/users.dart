class Users {
  final int userID;
  final String name;
  final String email;
  final String password;
  final String profilePic;
  final String bio;
  final double reputationScore;

  Users({
    required this.userID,
    required this.name,
    required this.email,
    required this.password,
    required this.profilePic,
    required this.bio,
    required this.reputationScore,
  });
}
